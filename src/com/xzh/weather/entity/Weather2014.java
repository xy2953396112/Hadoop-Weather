package com.xzh.weather.entity;

public class Weather2014 {
    private Integer id;

    private String city;

    private String htemp;

    private String ltemp;

    private String rain;

    private String wind;

    private Integer humidity;

    private String air;

    private String date;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getHtemp() {
        return htemp;
    }

    public void setHtemp(String htemp) {
        this.htemp = htemp;
    }

    public String getLtemp() {
        return ltemp;
    }

    public void setLtemp(String ltemp) {
        this.ltemp = ltemp;
    }

    public String getRain() {
        return rain;
    }

    public void setRain(String rain) {
        this.rain = rain;
    }

    public String getWind() {
        return wind;
    }

    public void setWind(String wind) {
        this.wind = wind;
    }

    public Integer getHumidity() {
        return humidity;
    }

    public void setHumidity(Integer humidity) {
        this.humidity = humidity;
    }

    public String getAir() {
        return air;
    }

    public void setAir(String air) {
        this.air = air;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}