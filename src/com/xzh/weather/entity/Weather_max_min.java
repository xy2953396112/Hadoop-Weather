package com.xzh.weather.entity;

public class Weather_max_min {
    
	private String city;
	private String htemp;
	private String ltemp;
	private String humidityMax;
	private String humidityMin;
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getHtemp() {
		return htemp;
	}
	public void setHtemp(String htemp) {
		this.htemp = htemp;
	}
	public String getLtemp() {
		return ltemp;
	}
	public void setLtemp(String ltemp) {
		this.ltemp = ltemp;
	}
	public String getHumidityMax() {
		return humidityMax;
	}
	public void setHumidityMax(String humidityMax) {
		this.humidityMax = humidityMax;
	}
	public String getHumidityMin() {
		return humidityMin;
	}
	public void setHumidityMin(String humidityMin) {
		this.humidityMin = humidityMin;
	}
	
}
