package com.xzh.weather.entity;

public class Weather_avg {
    
	private String city;
	private String htempAvg;
	private String ltempAvg;
	private String humidity;
	private String airCount;
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getHtempAvg() {
		return htempAvg;
	}
	public void setHtempAvg(String htempAvg) {
		this.htempAvg = htempAvg;
	}
	public String getLtempAvg() {
		return ltempAvg;
	}
	public void setLtempAvg(String ltempAvg) {
		this.ltempAvg = ltempAvg;
	}
	public String getHumidity() {
		return humidity;
	}
	public void setHumidity(String humidity) {
		this.humidity = humidity;
	}
	public String getAirCount() {
		return airCount;
	}
	public void setAirCount(String airCount) {
		this.airCount = airCount;
	}
	
}
